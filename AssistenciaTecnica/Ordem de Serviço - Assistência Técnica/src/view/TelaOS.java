    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       OS os = new OS();
       OsDAO dao = new OsDAO();
       
       os.setTipo((String) (cbTipo.getSelectedItem()));
       os.setSituacao((String) (cbSituacao.getSelectedItem()));
       os.setEquipamento(txtEquipamento.getText());
       os.setDefeito(txtDefeito.getText());
       os.setServico(txtServico.getText());
       os.setTecnico(txtTecnico.getText());
       os.setValor(Integer.parseInt(txtValor.getText()));
       os.setNomecli((txtCliId.getText()));
       
       
       
      if( (txtCliente.getText().equals("   -   -   -  ")) ||(txtCliId.getText().isEmpty()) || (txtEquipamento.getText().isEmpty()) || (txtDefeito.getText().isEmpty()) || (txtServico.getText().isEmpty()) || (txtTecnico.getText().isEmpty()) || (txtValor.getText().isEmpty()) || (cbTipo.getSelectedItem().equals("-Selecione-"))  || (cbSituacao.getSelectedItem().equals("-Selecione-"))){
           JOptionPane.showMessageDialog(null, "Preencha todos os campos");
      }else{
      
       dao.adicionar(os);
       
       
       cbTipo.setSelectedItem("");
       cbSituacao.setSelectedItem("");
       txtEquipamento.setText("");
       txtDefeito.setText("");
       txtServico.setText("");
       txtTecnico.setText("");
       txtValor.setText("");
       txtCliId.setText("");
       
       
       readJTable();
       
     
       
      
       JOptionPane.showMessageDialog(null,"Cadastrado com sucesso");
      }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          if (txtCliente.getText().length() > 0) {
            try {
                String consulta = "select * from clientes where cpf = '" + this.txtCliente.getText() + "'";
                PreparedStatement stmt = con.prepareStatement(consulta);
                rs = stmt.executeQuery(consulta);
                if (rs.next()) {
                    String nome = rs.getString("nomecli");
                    txtCliId.setText(nome);
                } else {
                    JOptionPane.showMessageDialog(null, "Nome não encontrado");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao buscar");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Campo Nome vázio");
        }
          
                  
    
    }//GEN-LAST:event_jButton1ActionPerformed

