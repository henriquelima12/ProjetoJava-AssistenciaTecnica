    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       OS os = new OS();
       OsDAO dao = new OsDAO();
       
       
       os.setTipo((String) (cbTipo.getSelectedItem()));
       os.setSituacao((String) (cbSituacao.getSelectedItem()));
       os.setEquipamento(txtEquipamento.getText());
       os.setDefeito(txtDefeito.getText());
       os.setServico(txtServico.getText());
       os.setTecnico(txtTecnico.getText());
       os.setValor(Integer.parseInt(txtValor.getText()));
       os.setNomecli((txtCliId.getText()));
       os.setOs((int) jTable2.getValueAt(jTable2.getSelectedRow(), 0));
       
       
       dao.atualizar(os);
       
       cbTipo.setSelectedItem("");
       cbSituacao.setSelectedItem("");
       txtEquipamento.setText("");
       txtDefeito.setText("");
       txtServico.setText("");
       txtTecnico.setText("");
       txtValor.setText("");
       txtCliId.setText("");
       
       
       readJTable();
       
     
       
      
       JOptionPane.showMessageDialog(null,"Atualizado com sucesso");
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
         if(jTable2.getSelectedRow() != -1){
       
       OS os = new OS();
       OsDAO dao = new OsDAO();
       
      
       os.setOs((int) jTable2.getValueAt(jTable2.getSelectedRow(), 0));
       
       dao.deletar(os);
       
       cbTipo.setSelectedItem("");
       cbSituacao.setSelectedItem("");
       txtEquipamento.setText("");
       txtDefeito.setText("");
       txtServico.setText("");
       txtTecnico.setText("");
       txtValor.setText("");
       txtCliId.setText("");
       
       
       readJTable();
       
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um usuário para excluir");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

